    public void GenerujPole(){
        for(int x = 0;x<this.PocetPolicek;x++){
            for(int y = 0;y<this.PocetPolicek;y++){
                pole[x][y] = 0;
            }
        }

        //System.out.println("Pole bylo uspesne vytvoreno!");
    }

    public void GenerujMiny(){
        Random nahoda = new Random();


        for(int x = 0;x<this.PocetMin;x++){
            int nxPozice,nyPozice;
            
            nxPozice= nahoda.nextInt(this.PocetPolicek);
            nyPozice= nahoda.nextInt(this.PocetPolicek);

            while(pole[nxPozice][nyPozice] == 1){
                nxPozice = nahoda.nextInt(this.PocetPolicek);
                nyPozice = nahoda.nextInt(this.PocetPolicek);
            }

            pole[nxPozice][nyPozice] = 1;
            System.out.println(Integer.toString(nxPozice)+"-"+Integer.toString(nyPozice));
        } 
        
        //System.out.println("Miny byly uspesne rozmisteny!");
    }

    public int Tahni(int X,int Y){
        if(pole[X][Y]==1){
            return 1;
        }else if(pole[X][Y]==2){
            return 2;
        }else{
            return 0;
        }
    }

    public void VypisSit(){
        for(int z = 0;z<this.PocetPolicek;z++){
            System.out.print("\t"+z);
        }
        System.out.println();
        for(int x = 0;x<this.PocetPolicek;x++){
            System.out.print(x+"");
            for(int y = 0;y<this.PocetPolicek;y++){
                if(pole[x][y]==2){
                    int KamC = PocetKamaradu(x,y);
                    System.out.print("\t"+KamC);
                }else{
                    System.out.print("\t.");
                }

            }
            System.out.println();
        }
        System.out.println("--------------------------------------------------------------");
    }

    public void ZmenViditelnost(int x,int y){
        pole[x][y] = 2;
    }

    public void OdkryjSit(){
        for(int z = 0;z<this.PocetPolicek;z++){
            System.out.print("\t"+z);
        }
        System.out.println();
        for(int x = 0;x<this.PocetPolicek;x++){
            System.out.print(x+"");
            for(int y = 0;y<this.PocetPolicek;y++){
                if(pole[x][y]==1){
                    System.out.print("\t*");
                }else{
                    int KamC = PocetKamaradu(x,y);
                    System.out.print("\t"+KamC);
                }

            }
            System.out.println();
        }
        System.out.println("--------------------------------------------------------------");
    }

    public int PocetKamaradu(int pX,int pY){
        int friends = 0;
        
            // horni rada
            try{
                friends+= (pole[pX-1][pY-1]==1 ? 1:0);
            }catch(Exception e){}

            try{
            friends+= (pole[pX-1][pY]==1 ? 1:0);
            }catch(Exception e){}

            try{
            friends+= (pole[pX-1][pY+1]==1 ? 1:0);
            }catch(Exception e){}

            // stred
            try{
            friends+= (pole[pX][pY-1]==1 ? 1:0);
            }catch(Exception e){}

            try{
            friends+= (pole[pX][pY+1]==1 ? 1:0);
            }catch(Exception e){}

            // horni rada
            try{
            friends+= (pole[pX+1][pY-1]==1 ? 1:0);
            }catch(Exception e){}

            try{
            friends+= (pole[pX+1][pY]==1 ? 1:0);
            }catch(Exception e){}

            try{
            friends+= (pole[pX+1][pY+1]==1 ? 1:0);
            }catch(Exception e){}

            return friends;
    }

