    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int tahX,tahY;
        int tahnout = 1;
        
        try{
            tahX = Integer.parseInt(PoziceX.getText());
        }catch (Exception e){
            zobrazVarovani("Spatne zadana pozice X","Varovani");
            tahX = 0;
            tahnout = 0;
        }
        
        try{
            tahY = Integer.parseInt(PoziceY.getText());
        }catch (Exception e){
            tahY = 0;
            tahnout = 0;
            zobrazVarovani("Spatne zadana pozice Y","Varovani");
        }

        if(tahnout==1){
            if(tahX>=this.PocetPolicek || tahY>=this.PocetPolicek){
                zobrazVarovani("Tahate mimo rozsah!","Varovani");
            }else{

                try{
                    int tah = Tahni(tahX,tahY);

                       if(tah==0){
                            ZmenViditelnost(tahX,tahY);
                            VypisSit();
                        ZvysTahy();
                        }else if(tah==2){
                             zobrazHlasku("Tam uz si tahnul! Zkus to znova.","Informace");
                        }else{
                            zobrazHlasku("BOOOOOM!","Informace");
                            ZvysTahy();
                            OdkryjSit();
                            jButton2.setEnabled(false);
                            PoziceX.setEnabled(false);
                            PoziceY.setEnabled(false);
                            String tahy = pocetTahu.getText();
                            zobrazHlasku("Prave jste skoncil.! Ale dekuji vam za hru.\nVas pocet tahu byl: "+tahy,"Konec hry");
                        }

                }catch (Exception e){
                    zobrazChybu("Nepodarilo se tahnout! Vnitrni chyba systemu.","FATAL ERROR");
                }

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

