    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  
        boolean pust = true;

        try{
            this.PocetPolicek = Integer.parseInt(policek.getText());
        }catch (Exception e){
            zobrazChybu("Nelze deklarovat pocet policek! Pravdepodobne to neni cislo.","Chyba");
            pust = false;
        }
        try{
            this.PocetMin = Integer.parseInt(min.getText());
        }catch (Exception e){
            zobrazChybu("Nelze deklarovat pocet min! Pravdepodobne to neni cislo.","Chyba");
            pust = false;
        }

        int suma = this.PocetPolicek*this.PocetPolicek;
        if(suma<this.PocetMin){
            pust = false;
            zobrazVarovani("Je vice min nez policek!","Varovani");
        }

        if(this.PocetPolicek>this.maxPolicek || this.PocetPolicek<this.minPolicek){
            pust = false;
            zobrazVarovani("Sit neni z intervalu <10,50>","Varovani");
        }

        if(pust==true){

            policek.setEditable(false);
            min.setEditable(false);
            jButton1.setEnabled(false);
            jButton2.setEnabled(true);
            PoziceX.setEnabled(true);
            PoziceY.setEnabled(true);

            pole = new int[this.PocetPolicek][this.PocetPolicek];

            try{
                GenerujPole();
            }catch (Exception e){
                zobrazChybu("Vyskytla se chyba pri vytvareni pole","Chyba");
            }


            try{
                GenerujMiny();
            }catch (Exception e){
                zobrazChybu("Vyskytla se chyba pri generovani min","Chyba");
            }

            try{
                VypisSit();
            }catch (Exception e){
                zobrazChybu("Vyskytla se chyba pri vypisovani site!","Chyba");
            }

        }
}//GEN-LAST:event_jButton1ActionPerformed

